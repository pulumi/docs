#!/usr/bin/env node
// Fetch pulumi/pulumi star count and write data/github_stars.yml.
// Resilient: never fails the build — on error, leaves any existing file in place,
// or writes a sensible fallback on first run.

const fs = require("fs");
const path = require("path");

const REPO = "pulumi/pulumi";
const API_URL = `https://api.github.com/repos/${REPO}`;
const OUT = path.join(__dirname, "..", "data", "github_stars.yml");
const FALLBACK_COUNT = 25000;

function formatCompact(n) {
    if (n < 1000) return String(n);
    if (n < 100_000) {
        const v = n / 1000;
        return `${v.toFixed(1).replace(/\.0$/, "")}K`;
    }
    if (n < 1_000_000) return `${Math.round(n / 1000)}K`;
    const v = n / 1_000_000;
    return `${v.toFixed(1).replace(/\.0$/, "")}M`;
}

function write(count, fetched) {
    const display = formatCompact(count);
    const lines = [
        "# Generated by scripts/fetch-github-stars.js — do not edit by hand.",
        `count: ${count}`,
        `display: "${display}"`,
        `updated_at: ${new Date().toISOString()}`,
        `fetched: ${fetched ? "true" : "false"}`,
        "",
    ];
    fs.mkdirSync(path.dirname(OUT), { recursive: true });
    fs.writeFileSync(OUT, lines.join("\n"));
    console.log(`github-stars: wrote ${OUT} (count=${count}, display=${display}, fetched=${fetched})`);
}

async function main() {
    const headers = {
        "User-Agent": "pulumi-docs-build",
        Accept: "application/vnd.github+json",
    };
    if (process.env.GITHUB_TOKEN) {
        headers.Authorization = `Bearer ${process.env.GITHUB_TOKEN}`;
    }

    try {
        const controller = new AbortController();
        const timeout = setTimeout(() => controller.abort(), 10_000);
        const res = await fetch(API_URL, { headers, signal: controller.signal });
        clearTimeout(timeout);

        if (!res.ok) {
            throw new Error(`HTTP ${res.status} ${res.statusText}`);
        }
        const body = await res.json();
        const count = body.stargazers_count;
        if (typeof count !== "number") {
            throw new Error(`unexpected response: stargazers_count is ${typeof count}`);
        }
        write(count, true);
    } catch (err) {
        console.warn(`github-stars: fetch failed (${err.message}).`);
        if (fs.existsSync(OUT)) {
            console.warn(`github-stars: keeping existing ${OUT}.`);
            return;
        }
        console.warn(`github-stars: writing fallback (count=${FALLBACK_COUNT}).`);
        write(FALLBACK_COUNT, false);
    }
}

main();
