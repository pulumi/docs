/**
 * Generate Tailwind v4 @theme color variables from @pulumi/design-tokens JSON.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createRequire } from "node:module";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const THEME_ROOT = path.join(__dirname, "..");
const require = createRequire(path.join(THEME_ROOT, "package.json"));

const primitives = require("@pulumi/design-tokens/tokens/core/primitives.json");
const semantic = require("@pulumi/design-tokens/tokens/core/semantic.json");

const OUT_FILE = path.join(THEME_ROOT, "src/generated/tailwind-v4/_theme.scss");

const HEADER = `// Copyright 2026, Pulumi Corporation. All rights reserved.
// GENERATED BY theme/scripts/build-color-theme.mjs — DO NOT EDIT BY HAND.
`;

function resolveRef(value) {
    if (typeof value !== "string") return value;
    const paletteMatch = value.match(/^\{([a-z]+)\.(\d+)\}$/);
    if (paletteMatch) {
        const [, family, shade] = paletteMatch;
        const hex = primitives.palettes[family]?.[shade];
        if (!hex) throw new Error(`Unknown palette reference: ${value}`);
        return hex;
    }
    const utilityMatch = value.match(/^\{utility\.([a-z-]+)\}$/);
    if (utilityMatch) {
        const hex = primitives.utility?.[utilityMatch[1]];
        if (!hex) throw new Error(`Unknown utility reference: ${value}`);
        return hex;
    }
    return value;
}

function generateTailwindV4ThemeScss() {
    const { palettes, white, black } = primitives;
    const lines = [
        `${HEADER}
// Tailwind v4 @theme — colors from @pulumi/design-tokens/tokens/core/primitives.json
@theme {
    --color-white: ${white};
    --color-black: ${black};
    --color-service-black: ${primitives.utility["service-black"]};
    --color-transparent: transparent;
`,
    ];

    for (const [family, scale] of Object.entries(palettes)) {
        for (const [shade, value] of Object.entries(scale)) {
            lines.push(`    --color-${family}-${shade}: ${value};`);
        }
    }

    const s = {
        violet: palettes.violet,
        gray: palettes.gray,
        yellow: palettes.yellow,
        blue: palettes.blue,
        red: palettes.red,
        orange: palettes.orange,
        green: palettes.green,
    };

    lines.push("");
    lines.push(`    --color-violet-primary: ${resolveRef(semantic.tokens["brand-violet"])};`);
    lines.push("");
    lines.push(`    --color-violet-background: ${s.violet["50"]};`);
    lines.push(`    --color-violet-muted: ${s.violet["200"]};`);
    lines.push(`    --color-violet-accent: ${s.violet["500"]};`);
    lines.push("");
    lines.push(`    --color-gray-background: ${s.gray["50"]};`);
    lines.push(`    --color-gray-muted: ${s.gray["200"]};`);
    lines.push(`    --color-gray-accent: ${s.gray["500"]};`);
    lines.push(`    --color-gray-primary: ${s.gray["700"]};`);
    lines.push("");
    lines.push(`    --color-yellow-background: ${s.yellow["50"]};`);
    lines.push(`    --color-yellow-muted: ${s.yellow["200"]};`);
    lines.push(`    --color-yellow-accent: ${s.yellow["500"]};`);
    lines.push(`    --color-yellow-primary: ${s.yellow["700"]};`);
    lines.push("");
    lines.push(`    --color-blue-background: ${s.blue["50"]};`);
    lines.push(`    --color-blue-muted: ${s.blue["200"]};`);
    lines.push(`    --color-blue-accent: ${s.blue["500"]};`);
    lines.push(`    --color-blue-primary: ${s.blue["700"]};`);
    lines.push("");
    lines.push(`    --color-red-background: ${s.red["50"]};`);
    lines.push(`    --color-red-muted: ${s.red["200"]};`);
    lines.push(`    --color-red-accent: ${s.red["500"]};`);
    lines.push(`    --color-red-primary: ${s.red["700"]};`);
    lines.push("");
    lines.push(`    --color-orange-background: ${s.orange["50"]};`);
    lines.push(`    --color-orange-muted: ${s.orange["200"]};`);
    lines.push(`    --color-orange-accent: ${s.orange["500"]};`);
    lines.push(`    --color-orange-primary: ${s.orange["700"]};`);
    lines.push("");
    lines.push(`    --color-green-background: ${s.green["50"]};`);
    lines.push(`    --color-green-muted: ${s.green["200"]};`);
    lines.push(`    --color-green-accent: ${s.green["500"]};`);
    lines.push(`    --color-green-primary: ${s.green["700"]};`);
    lines.push("");
    lines.push(`    --font-display: Inter, ui-sans-serif, system-ui, sans-serif;`);
    lines.push(`    --font-body: Inter, ui-sans-serif, system-ui, sans-serif;`);
    lines.push(`    --font-mono: "Monaspace Neon Var", ui-monospace, SFMono-Regular, monospace;`);
    lines.push(`    --default-font-feature-settings: "liga" 1, "calt" 1, "cv11" 1;`);
    lines.push("");
    lines.push(`    --breakpoint-sm: 640px;`);
    lines.push(`    --breakpoint-md: 768px;`);
    lines.push(`    --breakpoint-lg: 1024px;`);
    lines.push(`    --breakpoint-xl: 1280px;`);
    lines.push(`    --breakpoint-xxl: 1536px;`);
    lines.push(`    --shadow-3xl: 0 35px 70px -20px rgba(0, 0, 0, 0.5);`);
    lines.push(`    --leading-extra-tight: 1.1;`);
    lines.push("}");

    return lines.join("\n");
}

function main() {
    fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
    fs.writeFileSync(OUT_FILE, generateTailwindV4ThemeScss(), "utf8");
    console.log(`Wrote ${OUT_FILE}`);
}

main();
